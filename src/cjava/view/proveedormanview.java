    private void adicionar() {
        
          pr = new ProveedorDTO();
        //ASIGNAR VALORES AL OBJ PRO
        pr.setRazonSocial(txtRazon.getText());
        pr.setDireccion(txtDireccion.getText());
        pr.setRuc(txtRuc.getText());
        pr.setTelefono(txtTelefono.getText());

        try {
            if (pro.ProveedorAdicionar(pr)) {//DEVUELVE UN BOOLEANO, SI ProductoAdicionar ES VERDADERO
                //txtCodigo.setText(pr.getIdProveedor());
                JOptionPane.showMessageDialog(null, "Proveedor registrado con Exito");
                LimpiaControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
       
    }

    private void modificar() {
        
         pr = new ProveedorDTO();
        //ASIGNAR VALORES AL OBJ PRO
        pr.setIdProveedor(txtCodigo.getText());
        pr.setRazonSocial(txtRazon.getText());
        pr.setDireccion(txtDireccion.getText());
        pr.setRuc(txtRuc.getText());
        pr.setTelefono(txtTelefono.getText());
        

        try {
            if (pro.ProveedorActualizar(pr)) {//DEVUELVE UN BOOLEANO, SI ProductoAdicionar ES VERDADERO
                //txtCodigo.setText(pr.getIdProveedor());
                JOptionPane.showMessageDialog(null, "Proveedor Actualizado con Exito");
                LimpiaControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        
    }

    private void eliminar() {
        
        pr = new ProveedorDTO();
       
        pr.setIdProveedor(txtCodigo.getText());

        try {
            if (pro.ProveedorEliminar(pr)) {//DEVUELVE UN BOOLEANO, SI ProductoAdicionar ES VERDADERO
                //txtCodigo.setText(pr.getIdProveedor());
                JOptionPane.showMessageDialog(null, "Proveedor Eliminado con Exito");
               
                LimpiaControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
     
    }

    private void buscar() {
        
        
         try {
             
            pr = pro.ProveedorBuscar(txtCodigo.getText());
            
            if(pr!=null)
            {
            txtRazon.setText(pr.getRazonSocial());
            txtRuc.setText(pr.getRuc());
            txtDireccion.setText(pr.getDireccion());
            txtTelefono.setText(pr.getTelefono());
           
                
            }
            
            else{
            JOptionPane.showMessageDialog(null, "Proveedor no existe");
            }
        } catch (Exception e) {
        }
        
    }

    private void LimpiaControles() {
        txtCodigo.setText("");
        txtRazon.setText("");
        txtDireccion.setText("");
        txtRuc.setText("");
        txtTelefono.setText("");
    }
